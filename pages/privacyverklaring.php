<h3>Privacyverklaring</h3>

<div style="max-width:680px;font-size:13px;line-height:1.7;color:var(--color-text);">

<p style="color:#5a7a90;">Laatst bijgewerkt: maart 2026</p>

<h4 style="margin-top:18px;">1. Algemeen</h4>
<p>Supradatabase.nl is een hobbyproject: een registry van in Nederland geregistreerde Toyota Supras. Deze website respecteert de privacy van bezoekers en gaat zorgvuldig om met persoonsgegevens. In deze verklaring staat welke gegevens worden verzameld, waarom, hoe lang ze worden bewaard en wat uw rechten zijn.</p>

<h4 style="margin-top:18px;">2. Verzamelde gegevens</h4>
<p>De website verwerkt de volgende gegevens:</p>
<ul>
    <li><strong>IP-adres bij paginabezoeken</strong> — voor bezoekstatistieken en bescherming tegen misbruik.</li>
    <li><strong>IP-adres bij het plaatsen van een reactie</strong> — voor spam- en misbruikpreventie.</li>
    <li><strong>Naam bij reacties</strong> — alleen indien vrijwillig ingevuld; invullen is niet verplicht.</li>
    <li><strong>Tekst van reacties</strong> — de inhoud die een gebruiker zelf indient.</li>
    <li><strong>Eigenaarsnaam bij auto-invoer</strong> — alleen indien vrijwillig opgegeven door de eigenaar of indiener van een voertuig. De eigenaar kan deze naam zelf aanpassen of verwijderen via de bewerkingspagina.</li>
</ul>
<p>Foto-uploads worden anoniem verwerkt: er worden geen persoonsgegevens opgeslagen die aan een upload zijn gekoppeld.</p>

<h4 style="margin-top:18px;">3. Doel van gegevensverwerking</h4>
<ul>
    <li>IP-adressen bij paginabezoeken: inzicht in populaire pagina's en detectie van geautomatiseerd verkeer.</li>
    <li>IP-adressen bij reacties: voorkomen van spam en misbruik (maximaal 3 reacties per IP per 24 uur).</li>
    <li>Namen en reactieteksten: weergave van de reactie op de betreffende autopagina.</li>
    <li>Eigenaarsnamen: identificatie van de eigenaar van een voertuig in de registry, uitsluitend op verzoek of met instemming van de eigenaar zelf.</li>
</ul>

<h4 style="margin-top:18px;">4. Bewaartermijnen</h4>
<ul>
    <li><strong>IP-adressen (paginabezoeken):</strong> worden automatisch verwijderd na 90 dagen.</li>
    <li><strong>IP-adressen (reacties):</strong> worden bewaard zolang de bijbehorende reactie actief is op de website. Reacties worden niet automatisch verwijderd.</li>
    <li><strong>Reacties en namen:</strong> worden bewaard totdat de beheerder of de indiener om verwijdering verzoekt.</li>
    <li><strong>Eigenaarsnamen:</strong> worden bewaard zolang het voertuig in de registry staat, tenzij eerder verwijdering wordt verzocht.</li>
</ul>

<h4 style="margin-top:18px;">5. Cookies</h4>
<p>Deze website maakt uitsluitend gebruik van functionele cookies, zoals een sessiecookie voor ingelogde beheerders en een cookie voor de themakeuze (licht/donker). Er worden geen tracking- of marketingcookies geplaatst.</p>

<h4 style="margin-top:18px;">6. Ingesloten video's (YouTube &amp; Vimeo)</h4>
<p>Reacties kunnen een link naar een YouTube- of Vimeo-video bevatten. Deze video's worden <strong>niet automatisch geladen</strong>: pas nadat u op de videoafbeelding klikt, wordt er verbinding gemaakt met YouTube (<a href="https://www.youtube-nocookie.com" target="_blank" rel="noopener">youtube-nocookie.com</a>) of Vimeo. Op dat moment kunnen die diensten gegevens verwerken conform hun eigen privacybeleid. Zolang u niet klikt, worden er geen gegevens uitgewisseld met deze partijen.</p>

<h4 style="margin-top:18px;">7. Delen van gegevens</h4>
<p>Persoonsgegevens worden niet gedeeld met derden, tenzij dit wettelijk verplicht is.</p>

<h4 style="margin-top:18px;">8. Beveiliging</h4>
<p>Er worden passende technische maatregelen genomen om gegevens te beschermen, waaronder het gebruik van beveiligde formulieren (CSRF-beveiliging), invoervalidatie en beperkte toegang tot beheerdersfuncties.</p>

<h4 style="margin-top:18px;">9. Uw rechten</h4>
<p>U heeft het recht op inzage, correctie of verwijdering van uw persoonsgegevens. Verzoeken hiervoor kunt u indienen via de contactgegevens op de <a href="index.php?navigate=address">contactpagina</a>.</p>

<h4 style="margin-top:18px;">10. Wijzigingen</h4>
<p>Deze privacyverklaring kan worden aangepast wanneer de website verandert. De datum bovenaan deze pagina geeft de meest recente versie aan.</p>

