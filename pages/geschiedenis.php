<BR><BR><h3>Over de SNLDB</h3><BR><BR>
</center>
De naam SNLDB ("Supra NL Database") is een initiatief van TSE @ supras.nl. <BR>
TSE had een supra. Een echte originele witte non turbo automaat MKIII.<BR><BR>
Nou ja een echte...daar schortte wel wat aan.Onder meer een turbo en echte lak. Vandaar dat hij hem na wat jaren verkocht.<BR><BR>
<BR>Echte roots laten zich niet verloochenen en hij kocht een honda CRX. De database die hij sinds 2003 zo trouw onderhield kwam wat onder het stof en na een half jaar
afwezigheid heeft hij de database overgedragen aan het shoarmateam. <BR><BR>
Nu is de database weer terug en hopelijk kan supra rijdend Nederland nog lang genieten van zijn erfenis.<BR><br>

